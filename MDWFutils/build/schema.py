def parse_build_params(param_string: str) -> Dict[str, Any]:
    """Parse KEY=VALUE build params; supports comma-separated lists."""
    result: Dict[str, Any] = {}
    for token in (param_string or "").split():
        if "=" not in token:
            continue
        key, value = token.split("=", 1)
        key = key.strip()
        value = value.strip()
        if key == "hasenbusch":
            result[key] = [float(x) for x in value.split(",") if x]
        elif key in ("clean_install",):
            result[key] = value.lower() in ("1", "true", "yes")
        elif key in ("parallel_jobs", "ensemble_id"):
            result[key] = int(value)
        else:
            result[key] = value
    return result


def common_build_params() -> List[ContextParam]:
    return [
        ContextParam("clean_install", bool, default=True, help="Remove build/install dirs before build"),
        ContextParam("parallel_jobs", int, default=8, help="Parallel make jobs"),
        ContextParam("base", str, help="MDWF_SOFTWARE_ROOT override"),
        ContextParam("source_dir", str, help="Source directory"),
        ContextParam("build_dir", str, help="Build directory"),
        ContextParam("install_gpu_dir", str, help="GPU install prefix root"),
        ContextParam("install_cpu_dir", str, help="CPU install prefix root"),
        ContextParam("scripts_dir", str, help="Generated scripts directory"),
        ContextParam("perm_fix", str, help="Permission fix script path"),
        ContextParam("perm_group", str, help="Group for permission fix"),
        ContextParam("gmp_prefix", str, help="GMP install prefix"),
        ContextParam("mpfr_prefix", str, help="MPFR install prefix"),
        ContextParam("gpu_arch", str, help="QUDA GPU arch (e.g. sm_80)"),
        ContextParam("cray_pe_cc", str, help="Cray PE cc path for Grid GPU"),
    ]


def provenance_header(command: str, ensemble_id: int, action: str = "") -> str:
    ts = datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")
    lines = [
        f"# Generated by mdwf_db at {ts}",
        f"# Command: {command}",
        f"# Ensemble ID: {ensemble_id}",
    ]
    if action:
        lines.append(f"# Action: {action}")
    return "\n".join(lines) + "\n"


class BuildContextBuilder(ABC):
    """Base class for build script / source generators."""

    type_name: Optional[str] = None
    template_name: Optional[str] = None
    operation: str = ""
    package: str = ""
    build_params_schema: Optional[List[ContextParam]] = None

    def build(
        self,
        backend: Optional[DatabaseBackend],
        ensemble_id: int,
        build_params: Optional[Dict] = None,
        *,
        ensemble: Optional[Dict] = None,
        command_line: str = "",
    ) -> Dict:
        build_params = build_params or {}
        site_overrides = {
            k: build_params[k]
            for k in (
                "base",
                "source_dir",
                "build_dir",
                "install_gpu_dir",
                "install_cpu_dir",
                "scripts_dir",
                "perm_fix",
                "perm_group",
                "gmp_prefix",
                "mpfr_prefix",
                "gpu_arch",
                "cray_pe_cc",
            )
            if k in build_params and build_params[k] not in (None, "")
        }
        site = resolve_site_profile(site_overrides)
        if ensemble is None and backend is not None:
            ensemble = backend.get_ensemble(ensemble_id)
        return self._build_context(
            backend,
            ensemble_id,
            ensemble or {},
            site,
            build_params,
            command_line=command_line,
        )

    @abstractmethod
    def _build_context(
        self,
        backend: Optional[DatabaseBackend],
        ensemble_id: int,
        ensemble: Dict,
        site: SiteProfile,
        build_params: Dict,
        *,
        command_line: str = "",
    ) -> Dict:
        ...

    def base_context(
        self,
        ensemble_id: int,
        site: SiteProfile,
        build_params: Dict,
        *,
        command_line: str = "",
        install_prefix: str = "",
    ) -> Dict:
        ctx = {
            **site.as_dict(),
            "ensemble_id": ensemble_id,
            "operation": self.operation,
            "package": self.package,
            "clean_install": build_params.get("clean_install", True),
            "parallel_jobs": int(build_params.get("parallel_jobs", 8)),
            "install_prefix": install_prefix,
            "provenance": provenance_header(command_line or f"mdwf_db build {self.type_name}", ensemble_id),
        }
        return ctx

    @staticmethod
    def script_output(site: SiteProfile, prefix: str, scripts_subdir: str = "") -> Dict[str, str]:
        out_dir = Path(site.scripts_dir)
        if scripts_subdir:
            out_dir = out_dir / scripts_subdir
        return {
            "_output_dir": str(out_dir),
            "_output_prefix": prefix,
            "_output_suffix": ".sh",
            "_executable": True,
        }
